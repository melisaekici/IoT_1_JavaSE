package com.melisaekici;

public class _8_1_Math {
    public static void main(String[] args) {

        // Mathematics (Math)
        System.out.println("Pi Sayısı: " +Math.PI);

        System.out.println("Mutlak Değer: " +Math.abs(-25));
        System.out.println("Karekök Değer: " + Math.sqrt(36));
        System.out.println("Üslü Değer: " +Math.pow(2,5)); // 2^5

        System.out.println("Maksimum Değer: " +Math.max(1,500));
        System.out.println("Minimum Değer: " +Math.min(1,500));

        System.out.println("Sayıyı aşağıya yuvarlama: " +Math.floor(4.9));
        System.out.println("Sayıyı yukarı yuvarlama: " +Math.ceil(11.3));
    }
}
